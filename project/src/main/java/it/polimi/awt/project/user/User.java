package it.polimi.awt.project.user;

import javax.persistence.Column;
import javax.persistence.DiscriminatorColumn;
import javax.persistence.Entity;
import javax.persistence.EnumType;
import javax.persistence.Enumerated;
import javax.persistence.Id;
import javax.persistence.Inheritance;
import javax.persistence.InheritanceType;
import javax.persistence.Table;

import org.hibernate.annotations.Type;
import org.hibernate.annotations.TypeDef;

import it.polimi.awt.project.config.CustomEnumType;

@DiscriminatorColumn(name = "role")
@Entity
@Inheritance(strategy = InheritanceType.SINGLE_TABLE)
@Table(name = "user", schema = "public")
@TypeDef(name = "postgresql_enumeration", typeClass = CustomEnumType.class)
public abstract class User {

	@Id
	private String name;
	private boolean enabled;
	private String email;
	private String password;
	@Column(columnDefinition = "role", insertable = false, updatable = false)
	@Enumerated(EnumType.STRING)
	@Type(type = "postgresql_enumeration")
	private UserRole role;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public boolean isEnabled() {
		return enabled;
	}

	public void setEnabled(boolean enabled) {
		this.enabled = enabled;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}

	public UserRole getRole() {
		return role;
	}

	public void setRole(UserRole role) {
		this.role = role;
	}

}
