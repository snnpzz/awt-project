package it.polimi.awt.project.config;

import org.springframework.context.annotation.Configuration;
import org.springframework.core.Ordered;
import org.springframework.web.servlet.config.annotation.ViewControllerRegistry;
import org.springframework.web.servlet.config.annotation.WebMvcConfigurer;

@Configuration
public class WebMvcConfig implements WebMvcConfigurer {

	@Override
	public void addViewControllers(ViewControllerRegistry registry) {
		registry.addViewController("/").setViewName("index");
		registry.addViewController("/sign-in").setViewName("signin");
		registry.addViewController("/sign-up").setViewName("signup");
		registry.addViewController("/manager/new-campaign").setViewName("/manager/newcampaign");
		registry.setOrder(Ordered.HIGHEST_PRECEDENCE);
	}

}
